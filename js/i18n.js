function applyLang(newLang) {
  if (!I18N[newLang]) return;
  currentLang = newLang;
  
  document.querySelectorAll('#mp-lang-list .lang-btn').forEach(b => {
    b.classList.toggle('active', b.dataset.lang === newLang);
  });
  
  const codeEl = document.querySelector('.lang-current-code');
  if (codeEl) codeEl.textContent = newLang.toUpperCase();
  document.querySelectorAll('#mp-panel .mp-item').forEach(it => it.classList.remove('open'));
  
  document.documentElement.lang = newLang;
  document.documentElement.dir = newLang === 'ar' ? 'rtl' : 'ltr';
  
  const closeBtn = document.getElementById('dp-close');
  if (closeBtn) closeBtn.title = t('close');
  const lsLabel = document.getElementById('ls-label-text');
  if (lsLabel) lsLabel.textContent = t('level_label');
  const sBtn = document.querySelector('#mp-search .mp-btn');
  if (sBtn) sBtn.title = t('search_title');
  const sInp = document.getElementById('search-input');
  if (sInp) sInp.placeholder = t('search_placeholder');
  const poiTitle = document.getElementById('mp-poi-title');
  if (poiTitle) poiTitle.textContent = t('poi_title');
  const levelTitle = document.getElementById('mp-level-title');
  if (levelTitle) levelTitle.textContent = t('level_label');
  const lblSearch = document.getElementById('mp-search-label');
  if (lblSearch) lblSearch.textContent = t('mp_label_search');
  const lblLang   = document.getElementById('mp-lang-label');
  if (lblLang)   lblLang.textContent   = t('mp_label_lang');
  const lblPoi    = document.getElementById('mp-poi-label');
  if (lblPoi)    lblPoi.textContent    = t('mp_label_poi');
  const lblLevel  = document.getElementById('mp-level-label');
  if (lblLevel)  lblLevel.textContent  = t('level_label');
  const lblMask   = document.getElementById('mp-mask-label');
  if (lblMask)   lblMask.textContent   = t('mp_label_mask');
  const maskTitle = document.getElementById('mp-mask-title');
  if (maskTitle) maskTitle.textContent = t('mask_title');
  if (typeof renderMaskList === 'function') renderMaskList();
  if (typeof updateMaskCurrentLabel === 'function') updateMaskCurrentLabel();
  if (typeof renderPoiList === 'function') renderPoiList();
  if (typeof _refreshViewSwitchLabel === 'function') _refreshViewSwitchLabel();
  
  if (typeof renderChoro === 'function') renderChoro();
  
  const panel = document.getElementById('detail-panel');
  if (panel && panel.classList.contains('open')) {
    const titleEl = document.getElementById('dp-title');
    if (titleEl && titleEl.dataset.key) {
      const fs = panel.classList.contains('fullscreen');
      if (titleEl.dataset.key === '__dubai__') window.openDubai({fullscreen: fs});
      else window.openDistrictByKey(titleEl.dataset.key, fs);
    }
  }
}
